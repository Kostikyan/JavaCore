package telegramTasks.categoryFor;

public class ForKAndN {

    public static void main(String[] args) {
        // Задача 9: Даны целые числа K и N
        // ( N > 0). Вывести N раз число K
        double k = 23.4d;
        int n = 10;
        for (int i = 0; i < n + 1; i++) {
            System.out.println(i + ") " + k);
        }

    }

}
